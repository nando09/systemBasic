select * from pedindo


SELECT
	PR.ID AS ID,
	PR.NOME AS NOME,
	PR.VALOR AS VALOR,
	PR.DESCRICAO AS DESCRICAO,
	F.QUANTIDADE AS TEM
FROM
	FORNECENDO AS F
INNER JOIN
	PRODUTO AS PR
ON
	PR.ID = F.ID_PRODUTO
WHERE
	F.FINALIZADO = 'SIM'
AND
	F.ID_DETALHE = 4


SELECT
	COUNT(*) as NUMERO,
	SUM(P.VALOR * PE.QUANTIDADE) AS VALOR
FROM
	PEDINDO AS PE
INNER JOIN
	PRODUTO AS P
ON
	P.ID = PE.ID_PRODUTO
WHERE
	PE.FINALIZADO = 'SIM'
AND
	PE.ID_DETALHE = 46


